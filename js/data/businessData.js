/*
General business configuration rules

Key must always be the same as the name, and they must be unique
    example:    "some_random_name":{ "name":"some_random_name" }
Business `chain` must have a match in Chain at `name` and key
Business `type` must be "product" or "money"
Business `managerprice` by default is 50 times the business `price` but it can be set to whatever amount
Business `intake` must be array
Business `intakeamount` must be array and match the length of `intake` array
    example:    "intake":["item1","item2"],
                "intakeamount":[3,5],
Unlike `intake`, business can only have 1 `output` (and obviously 1 `outputamount`)
Business `chainposition` should start from 1 and increase one by one, even though it might work fine if it doesn't
Uncountable items will not have an "s" added to the end when user has more than 1. Items that require "es" can also be added here
*/

